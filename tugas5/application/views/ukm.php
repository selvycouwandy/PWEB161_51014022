<h1>Tentang UKM</h1>

<h4>UKM ini diberi nama "food & fun". Disini kita dapat bertukar pendapat dan berbagi pengalaman mengenai masakan yang pernah dibuat. Untuk saat ini anggota ukm berjumlah 42 orang.</h4>
<h4>Ketua UKM: William Tumewah</h4>
<h4>Sekretaris UKM: Kevin Changir</h4>
<h4>Pembimbing UKM: Ibu Husni Anggriani</h4>
<h4>Visi Misi</h4>
<h4>Visi UKM : dengan adanya ukm ini mahasiswa bisa menjadi lebih berpengalaman dalam mengolah masakan.</h4>
<h4>Misi UKM : menyelenggarakan kegiatan masak memasak antar mahasiwa dan saling mencicipi resep yang telah dimasak satu sama lain.</h4>