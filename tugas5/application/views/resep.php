
<h1>Resep Nasi Goreng</h1>

<h2>Bahan-bahan</h2>

<p>1 piring nasi putih</p>
<p>1 butir telur bebek (bisa telur ayam) digoreng dulu setengah matang</p>
<p>secukupnya sayuran : kubis, wortel</p>
<p>3 cabai rawit</p>
<p>1 siung bawang merah</p>
<p>1 siung bawang putih</p>
<p>garam secukupnya dan saus sambal secukupnya</p>
<p>secukupnya kecap manis </p>


<h2>Cara Membuat</h2>
<p>1. Panaskan 1 sendok margarin. Masukkan bawang putih dan bawang merah yang telah dihaluskan kemudian ditumis sampai bau harum.</p>
<p>2. Masukkan nasi lalu goreng sampai bumbu tercampur rata. Kemudian masukkan telur bersama sayur yang telah dipotong memanjang. Aduk sampai tercampur rata dengan nasi dan sayur nampak layu.</p>
<p>3. Beri garam saus cabai dan kecap secukupnya. Lalu angkat jika dirasa sudah matang dan semua tercampur rata.</p>
<p>4. Sajikan diatas piring dan dimakan selagi panas. Selamat mencoba</p>
        
    
   





 
    
    
    
    
   